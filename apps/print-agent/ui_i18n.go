import "fmt"

// uiT returns UI copy for tray / wizards. Keys are stable; missing key returns key.
func uiT(locale, key string) string {
	loc := normalizeUILocale(locale)
	b := uiBundles[loc]
	if b == nil {
		b = uiBundles["zh"]
	}
	if s, ok := b[key]; ok {
		return s
	}
	return key
}

func uiBundleMap(locale string) map[string]string {
	loc := normalizeUILocale(locale)
	out := make(map[string]string, len(uiBundles["zh"]))
	for k, v := range uiBundles[loc] {
		out[k] = v
	}
	out["test_print_phrase"] = testPrintPhrase(loc)
	return out
}

func uiError(locale, key string, args ...any) error {
	msg := uiT(locale, key)
	if len(args) > 0 {
		msg = fmt.Sprintf(msg, args...)
	}
	return errString(msg)
}

